"""
Script: visualize_results.py
Project: Large-Scale Reaction-Diffusion Simulation

Description:
Loads raw.npy state matrices generated by the explicit numerical solver 
and visualizes the temporal evolution of the Gray-Scott reaction-diffusion 
system, highlighting traveling waves and steady-state Turing patterns.
"""

import numpy as np
import matplotlib.pyplot as plt
import glob
import os
import json

def generate_result_grid(data_dir):
    """ Loads saved simulation states and generates a progression grid. """
    
    # Load experiment configuration
    with open(f"{data_dir}/experiment_config.json", 'r') as f:
        config = json.load(f)
        
    print(f"Loading data for config: Boundary={config['bc']}, F={config['F']}, k={config['k']}")
    
    # Find all saved state files and sort them chronologically
    files = sorted(glob.glob(f"{data_dir}/state_v_*.npy"))
    
    if not files:
        print("No simulation data found. Run the main solver first.")
        return

    # Select 4 evenly spaced snapshots to show progression
    indices = np.linspace(0, len(files) - 1, 4).astype(int)
    selected_files = [files[i] for i in indices]
    
    fig, axes = plt.subplots(1, 4, figsize=(20, 5))
    fig.suptitle(f"Reaction-Diffusion Evolution (Grid: {config['size']}x{config['size']}, BC: {config['bc'].capitalize()})", fontsize=16)

    for ax, file_path in zip(axes, selected_files):
        # Extract iteration step from filename
        step = int(file_path.split('_')[-1].split('.')[0])
        state_data = np.load(file_path)
        
        im = ax.imshow(state_data, cmap='inferno', vmin=0, vmax=1)
        ax.set_title(f"Iteration Step: {step}")
        ax.axis('off')
        
    cbar_ax = fig.add_axes([0.92, 0.15, 0.02, 0.7])
    fig.colorbar(im, cax=cbar_ax, label='Concentration of Species V')
    
    output_filename = f"simulation_results_{config['bc']}_Sept2025.png"
    plt.savefig(output_filename, dpi=300, bbox_inches='tight')
    print(f"Results successfully saved to {output_filename}")
    plt.show()

if __name__ == "__main__":
    # Point this to the output directory generated by the main solver
    target_directory = "SimOutputs_Sept2025_neumann_F0.035_k0.065"
    if os.path.exists(target_directory):
        generate_result_grid(target_directory)
    else:
        print(f"Directory {target_directory} not found.")